/*
* Programa de variables y constantes
*/
fun main() {
    // var es para variables, es decir valores que pueden cambiar a lo largo del programa
    var edadPerrito = 30
    println("La edad de tu perrito es: " + edadPerrito)
    edadPerrito = 31
    println("La edad de tu perrito ahora es: " + edadPerrito)

    // val es para constantes, es decir valores que nunca van a cambiar (read only)
    val nombrePerrito = "Miguel√≠n"
    println("El nombre de tu perrito es: " + nombrePerrito)
}